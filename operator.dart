void main() {
  int a = 4;
  int b = 10;
  int c = a + b;

  print(c);

  // Arithmetic Operator

  // addition
  int addition = a + b;
  print(addition);

  // subtraction
  int subtraction = a - b;
  print(subtraction);

  // multiplication
  int multiplication = a * b;
  print(multiplication);

  // division
  num division = a / b;
  print(division);

  // modulo
  num modulo = a % b;
  print(modulo);

  // equality and relational operators

  // more than
  print(a > b);

  // less than
  print(a < b);

  // less than
  print(a < b);

  // equals
  print(a == b);

  // not equal
  print(a != b);

  // more than or equal
  print(a >= b);

  // lees then or equal
  print(a <= b);

  // Logical Operators
  bool x = false;
  bool y = true;

  // && -> AND
  print(x & y);

  // || -> OR
  print(x || y);

  // ! -> NOT
  print(!x);
}
