void main() {
  String day = 'Monday';

  switch (day) {
    case 'Monday':
      print('Hari ini adalah Senin');
      break;
    case 'Tuesday':
      print('Hari ini adalah Selasa');
      break;
    case 'Wednesday':
      print('Hari ini adalah Rabu');
      break;
    case 'Thursday':
      print('Hari ini adalah Kamis');
      break;
    case 'Friday':
      print('Hari ini adalah Jumat');
      break;
    case 'Saturday':
      print('Hari ini adalah Sabtu');
      break;
    case 'Sunday':
      print('Hari ini adalah Minggu');
      break;
    default:
      print('Hari yang dimasukkan tidak valid');
  }
}
